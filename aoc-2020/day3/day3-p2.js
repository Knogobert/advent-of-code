function rideSlope(right, down) {
  console.log('right, down:', right, down)
  let rightTracker = 0;
  let downTracker = down;

  const output = input.split('\n').reduce((trees, row, rowIndex) => {
    if (rowIndex <= downTracker - 1) {
      console.log(`O i:${rowIndex}, EARLY:${downTracker}, row:${row}`)
      return trees; // skip downed rows
    }
    downTracker += down;
    rightTracker += right;

    const rowExtended = row.repeat(right * 12);
    // let consoleRow = rowExtended.split('');

    if (rowExtended[rightTracker] === '#') {
      // consoleRow[rightTracker] = 'X';
      // console.log(`X i:${rowIndex}, trees:${trees}, row:${consoleRow.join('')}`)
      console.log(`X i:${rowIndex}, trees:${trees}, row:${row}`)
      return ++trees;
    }

    // consoleRow[rightTracker] = 'O';
    // console.log(`O i:${rowIndex}, trees:${trees}, row:${consoleRow.join('')}`)
    console.log(`O i:${rowIndex}, trees:${trees}, row:${row}`)
    return trees;
  }, 0);

  console.log('_________________________output________________________:', output);
  return output;
}

